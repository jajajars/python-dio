saldo = 500 #Atribuição simples
print(saldo)

saldo += 200 #atribuição com adição
print(saldo)

saldo -= 100 #atribuição com subtração
print(saldo)

saldo *= 2 #atribuição com multiplicação
print(saldo)

saldo /= 3 #atribuição com divisão (float)
print(saldo)

saldo = 400
saldo //= 2 #atribuição com divisão (int)
print(saldo)

saldo %= 3 #atribuição com módulo. Retorna o módulo da variável no número atribuído
print(saldo) #saldo = 200. 200 mod 3 = 2

saldo **= 4 #atribuição com exponenciação
print(saldo)